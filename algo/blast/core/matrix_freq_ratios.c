SFreqRatios*
_PSIMatrixFrequencyRatiosFree(SFreqRatios* freq_ratios)
{
    if ( !freq_ratios )
        return NULL;

    if (freq_ratios->data) {
        _PSIDeallocateMatrix((void**) freq_ratios->data, BLASTAA_SIZE);
    }

    sfree(freq_ratios);
    return NULL;
}

